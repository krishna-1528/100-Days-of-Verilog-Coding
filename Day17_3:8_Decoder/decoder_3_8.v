module decoder_3_8(
//krishna
    input [2:0]S,
    input En,
    output [7:0]D
);

assign D = En ? ((S == 3'b000) ? 8'b0000_0001 :
                 (S == 3'b001) ? 8'b0000_0010 :
                 (S == 3'b010) ? 8'b0000_0100 :
                 (S == 3'b011) ? 8'b0000_1000 :
                 (S == 3'b100) ? 8'b0001_0000 :
                 (S == 3'b101) ? 8'b0010_0000 :
                 (S == 3'b110) ? 8'b0100_0000 :
                 (S == 3'b111) ? 8'b1000_0000 : 8'b0000_0000) : 8'b0000_0000;

endmodule
